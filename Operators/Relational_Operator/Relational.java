class Relational 
{
	public static void main(String[] args) 
	{
		System.out.println ( 10 == 10 ); //ture
		System.out.println ( 10 > 10 ); // false
		System.out.println ( 10 < 10 ); // false
		System.out.println ( 10 <= 10 ); // ture
		System.out.println ( 10 >= 10 ); // ture
		System.out.println ( 'a' > 'A' ); // ture
		System.out.println ( 10 != 10 ); // false
		
	}
}
