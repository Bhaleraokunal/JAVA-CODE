class Conditional2 
{
	public static void main(String[] args) 
	{
		// Find the even odd number without using the loops.?
		
		int num1 = 10000; // Taking Number for input 
		
		String ans = num1%2==0? "this is even" : "The given number is even"; // Use the conditional operator to get the expected output.
		
		System.out.println(ans); // Printing the output.
	}
}
